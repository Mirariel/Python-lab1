# Завдання 1: створення змінних і введення значень
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))
c = float(input("Введіть третє число: "))
d = float(input("Введіть четверте число: "))

# Завдання 2: виконання операцій
results = [
    a + b,
    a - c,
    b * d,
    c / a,
    b ** 2,
    d // a,
    c % b
]

# Завдання 3: кількість елементів і виведення парних
print("Кількість елементів у списку:", len(results))

print("Парні елементи списку:")
for item in results:
    if item % 2 == 0:
        print(item)

# Завдання 4: поміняти місцями 2-й і 5-й елементи
results[1], results[4] = results[4], results[1]
print("Список після обміну 2-го і 5-го елементів:")
print(results)

